#!/usr/bin/env node
/**
 * build-pack-contents.mjs — compile packs/<slug>/** into src/lib/pack-contents.ts.
 *
 * Reads every file under each packs/<slug>/ directory (recursively) and emits a
 * `PACK_FILES: Record<string, { path, content }[]>` map keyed by pack slug, where
 * each entry is { path (relative to packs/<slug>), content }. The download route
 * imports this map to serve gated zips.
 *
 * Tolerant of a missing packs/ dir (emits an empty map) so the build passes
 * before any packs are authored. Runs via `npm run build:packs` and, through the
 * `prebuild` npm script, automatically before `next build`.
 */
import { readdir, readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const PACKS_DIR = path.join(ROOT, "packs");
const OUT_FILE = path.join(ROOT, "src", "lib", "pack-contents.ts");

/** Recursively list all files under `dir`, sorted for stable output. */
async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries.sort((a, b) => a.name.localeCompare(b.name))) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walk(full)));
    } else if (entry.isFile()) {
      files.push(full);
    }
  }
  return files;
}

async function main() {
  /** @type {Record<string, { path: string, content: string }[]>} */
  const packFiles = {};

  if (existsSync(PACKS_DIR)) {
    const slugs = (await readdir(PACKS_DIR, { withFileTypes: true }))
      .filter((e) => e.isDirectory())
      .map((e) => e.name)
      .sort();

    for (const slug of slugs) {
      const packDir = path.join(PACKS_DIR, slug);
      const absFiles = await walk(packDir);
      const files = [];
      for (const abs of absFiles) {
        const rel = path.relative(packDir, abs).split(path.sep).join("/");
        const content = await readFile(abs, "utf8");
        files.push({ path: rel, content });
      }
      if (files.length > 0) packFiles[slug] = files;
    }
  }

  const banner = `/**
 * GENERATED by scripts/build-pack-contents.mjs — DO NOT EDIT BY HAND.
 *
 * Maps each pack slug to the files under packs/<slug>/ so the download route
 * can serve a gated zip. Regenerate with \`npm run build:packs\` (runs before
 * \`next build\` via the \`prebuild\` script).
 */
`;
  const body = `export const PACK_FILES: Record<string, { path: string; content: string }[]> = ${JSON.stringify(
    packFiles,
    null,
    2
  )};\n`;

  await writeFile(OUT_FILE, banner + body, "utf8");

  const packCount = Object.keys(packFiles).length;
  const fileCount = Object.values(packFiles).reduce((n, f) => n + f.length, 0);
  console.log(
    `build:packs → ${packCount} pack(s), ${fileCount} file(s) → src/lib/pack-contents.ts`
  );
}

main().catch((err) => {
  console.error("build:packs failed:", err);
  process.exit(1);
});
